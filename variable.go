package main

import "fmt"

func main() {
	var a string
	fmt.Println("Value of a is :", a)
	var b, c int = 34, 32
	fmt.Println(b, c)
	var name string = "Abu Said"
	fmt.Println(name)

	f := "family"
	fmt.Println(f)

	f = "said"
	fmt.Println(f)

	const d int = 3 // Declare constant variable with value
	fmt.Println(float64(d))
}
